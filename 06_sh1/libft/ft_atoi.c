#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int sign;
	int r;

	i = 0;
	r = 0;
	sign = 1;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t'
			|| str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		sign = (str[i] == '-') ? -1 : 1;
		i++;
	}
	if (!(str[i] >= '0' && str[i] <= '9'))
		return (0);
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = 10 * r + (str[i] - '0');
		i++;
	}
	return (sign * r);
}
